ACENTOS = { 'Á':'A','Ã':'A','À':'A','Â':'A', 
            'Ó':'O','Õ':'O','Ô':'O', 'Ò':'O',
            'É':'E','Ê':'E','È':'E',
            'Ú':'U','Ù':'U',
            'Ç':'C',
}

def tirar_acentos(message):
    cipher = ''
    for letter in message:
        if letter in ACENTOS:
            cipher += ACENTOS[letter]
        else:
            cipher += letter
    return cipher

def encrypt(message):
    message = tirar_acentos(message)
    cipher = ''
    for letter in message:
        if letter != ' ':
            cipher += MORSE_CODE_DICT[letter] + ' '
        else:
            cipher += ' / '
    return cipher